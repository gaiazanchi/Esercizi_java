package it.develhope;

/**
 * This class is made of all the arithmetic operators
 * @author Gaia Zanchi
 */
public class ArithmeticOperators {

    /**
     * Gives the sum of two numbers
     * @param num1 First integer given to the method
     * @param num2 Second integer given to the method
     * @return An integer that is the sum of num1 and num2
     */
    public int sum(int num1, int num2){
        return num1 + num2;
    }

    /**
     * Gives the difference of two numbers
     * @param num1 First integer given to the method
     * @param num2 Second integer given to the method
     * @return An integer that is the difference of num1 and num2
     */
    public int sub(int num1, int num2){
        return num1 - num2;
    }

    /**
     * Gives the product of two numbers
     * @param num1 First integer given to the method
     * @param num2 Second integer given to the method
     * @return An integer that is the product of num1 and num2
     */
    public int mul(int num1, int num2){
        return num1 * num2;
    }

    /**
     * Gives the division of two numbers
     * @param num1 First integer given to the method
     * @param num2 Second integer given to the method
     * @return An integer that is the division of num1 and num2
     */
    public int div(int num1, int num2){
        return num1 / num2;
    }

}
